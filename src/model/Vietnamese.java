
package model;

public class Vietnamese {
	private String vnese;

	public Vietnamese(String vnese) {
		this.vnese = vnese;
	}

	public String getVnese() {
		return vnese;
	}

	public void setVnese(String vnese) {
		this.vnese = vnese;
	}

	@Override
	public String toString() {
		return vnese;
	}
	
	
}
